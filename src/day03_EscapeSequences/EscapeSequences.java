package day03_EscapeSequences;

/*
  escape sequences: must be given with double quote
  \n: starts  a new line
  \t: paragraph sapce(tab)
  \\: single back slash
  \": double quote

 */

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPyhton \nC");

        System.out.println("---------------------------------------");

        System.out.println(" Hello Cydeo \nHow are you all you today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println(" -------------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("--------------------------------------");

        System.out.println("My favorite Tv show is \"Game Of Throne\""); // in order to print double quote, we need to use \"





    }


    }

